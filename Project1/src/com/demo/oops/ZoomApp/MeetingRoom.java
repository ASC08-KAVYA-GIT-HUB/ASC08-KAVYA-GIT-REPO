package com.demo.oops.ZoomApp;

public class MeetingRoom {
   private int roomId ;
   private int capacity;
   private int floor;
   //getters and setters
   void setRoomId(int roomId)
   {
      this.roomId=roomId;
   }

   int getRoomId()
   {
      return roomId;
   }
   void setCapacity(int capacity)
   {
      this.capacity=capacity;
   }
   int getCapacity()
   {
      return capacity;
   }
   void setFloor(int floor)
   {
      this.floor=floor;
   }
   int getFloor()
   {
      return floor;
   }






}
